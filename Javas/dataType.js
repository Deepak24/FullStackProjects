/**
 * Data Type
 */

//String

let strName = "Deepak";
console.log(strName);
console.log("Type of variable", typeof strName);

//Number

let intNum = 99;
console.log(intNum);
console.log("Type of intNum", typeof intNum);

//Boolean

let isProductInCart = false;
console.log(isProductInCart);
console.log("The type of variable", typeof isProductInCart);

//Undefined

let userName;
console.log(userName);
console.log(typeof userName);

//Null
let noValue = null;
console.log(noValue);
console.log(typeof noValue);

//object
let personDetails;

personDetails = {
    personName : "Deepak", age : 35, education : "MCA"
}

console.log(personDetails);
console.log(typeof personDetails);